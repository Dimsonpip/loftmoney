package com.dimsonpip.loftmoney.screens.web.models;

public class ItemRemote {

    private String id;
    private String name;
    private String price;
    private String type;
    private String date;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getPrice() {
        return price;
    }

    public String getType() {
        return type;
    }

    public String getDate() {
        return date;
    }

}
